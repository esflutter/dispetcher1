// chat_intent.dart — лёгкая эвристика «это запрос поиска или обычный вопрос?».
//
// Нужна, чтобы прямо из обычного чата с ассистентом можно было искать заказы
// (у исполнителя) / исполнителей и технику (у заказчика): если пользователь
// явно просит «найти заказы поблизости на неделю», уводим сообщение в поиск
// (карточки), а не в FAQ-ответ.
//
// Принципы:
//   • КОНСЕРВАТИВНО. Лучше пропустить пограничный запрос в обычный чат (там есть
//     контекст пользователя и подсказки), чем перехватить FAQ-вопрос и выдать
//     вместо ответа карточки. Полный разбор фильтров всё равно делает сервер
//     (ai-search) — здесь только грубое «это вообще поиск?».
//   • Чистые функции без состояния — легко покрыть юнит-тестами.
//
// ВАЖНО про кириллицу: \b в Dart RegExp работает только вокруг латиницы, поэтому
// границы слов задаём вручную — паддингом строки пробелами и (^|\s)…(\s|$).
// Стемы вроде «заказ» матчат «заказы/заказов/заказе» как подстроку — это ок.
library;

// «Мои/свои» заказы и «у меня …» — это личные принятые заказы (на них отвечает
// обычный чат с контекстом пользователя), а НЕ поиск по каталогу. Дательное
// «мне/меня» (найди мне…, покажи мне…) сюда НЕ включаем — это нормальный поиск.
final RegExp _possessive =
    RegExp(r'(^|\s)(мои|моих|моего|моих|моё|мой|моя|свои|своих|у меня|у мен)(\s|$)');

// Личные существительные. «Мои заказы», «у меня рейтинг», «мой профиль» — это
// вопрос про СВОИ данные (его ведёт обычный чат с контекстом). А «мой бюджет
// 5000», «в моём районе» — уточнение поиска, личного существительного рядом
// нет, поэтому из поиска НЕ выводим.
final RegExp _personalData = RegExp(
    r'(заказ|услуг|отклик|профил|карточк|подписк|верифик|рейтинг|'
    r'выплат|баланс|график|отзыв|оценк)');

// Вопросы-инструкции «как/почему/что такое…» — это FAQ. ЦЕНОВОЙ вопрос
// «сколько стоит …» вынесен отдельно (_priceQuery): «сколько стоит экскаватор»
// — это поиск техники с ценой, а «сколько стоит подписка» — FAQ.
final RegExp _faqHowto = RegExp(
    r'(^|\s)(как|почему|зачем|каким образом|что такое|что значит|можно ли|могу ли|'
    r'нужно ли|что делать|как мне)(\s|$|[,.?!])');
// «Сколько стоит …» — при названной технике это запрос цены на технику (поиск),
// без техники — FAQ про тарифы приложения.
final RegExp _priceQuery = RegExp(r'(^|\s)сколько стоит(\s|$|[,.?!])');
// Явная отмена/выход из пошагового сбора.
final RegExp _cancelCmd = RegExp(
    r'(^|\s)(стоп|отмена|отмени|отменить|выйти|выход|передума|'
    r'закончить|закончили|хватит)(\s|$|[,.!])');

// Слова про аккаунт пользователя — всегда FAQ (личный вопрос про приложение).
final RegExp _appAccount = RegExp(
    r'(подписк|верифик|карточк|профил|настройк|уведомл|'
    r'докумен|блокир|телефон|парол|аккаунт|вериф)');
// Рейтинг/оплата/тариф — это ФИЛЬТР поиска, если рядом названа техника
// («кран с рейтингом от 4.5»), и FAQ, если техники нет («как поднять рейтинг»).
final RegExp _appFilter = RegExp(r'(рейтинг|оплат|тариф)');

// Адвербиальные ОТВЕТЫ на «когда/как делать»: «как обычно», «как можно скорее»,
// «как угодно», «как раньше/всегда/прежде», «как в прошлый раз». Это ответы, а НЕ
// FAQ-вопрос «как …?», поэтому сбор НЕ прерываем. Список НАМЕРЕННО узкий — только
// слова, которые практически никогда не начинают настоящий вопрос. Раньше тут были
// «лучше/удобн/реши/сам/хочешь/принято», и они проглатывали реальные вопросы
// «как РЕШИть…», «как САМому…», «как ЛУЧШЕ оплатить…», «как самосвал отменить» —
// те уходили в сбор как ответ-слот вместо FAQ-ответа.
final RegExp _howAdverbAnswer = RegExp(
    r'(^|\s)как\s+(обычн|можно\s+скорее|можно\s+быстрее|угодно|раньше|'
    r'всегда|прежд|в\s+прошл|в\s+тот\s+раз)');

// Признак, что тема аккаунта поднята как ВОПРОС или ЖАЛОБА (а не мимоходом
// упомянута в ответе-слоте): знак вопроса, вопросительное слово, либо явная
// проблема (не работает / не вижу / отклонили / проблема / не прошла). Простое
// упоминание «карточка/телефон/подписка» в ответе вопросом не считаем — иначе
// при заполнении карточки слово «карточка» выкидывало бы из сбора.
final RegExp _questionMarker = RegExp(
    r'(\?'
    r'|(^|\s)(почему|зачем|сколько|когда|где|каким образом|что такое|'
    r'что значит|что за|можно ли|могу ли|нужно ли|расскаж|подскаж|объясн)'
    r'(\s|$|[,.?!])'
    r'|не\s+рабо|не\s+про(ш|шл)|не\s+получ|не\s+виж|не\s+вид|не\s+могу|'
    r'проблем|отклон|пропал|исчез|не\s+приход|не\s+пойм|не\s+понима)');

// Глаголы поиска. «подойд/подход» — «какой исполнитель мне подойдёт».
final RegExp _searchVerb = RegExp(
    r'(найд|найти|ищ[уи]|поищ|подбер|подыщ|подыскать|подойд|подход|'
    r'покаж|посмотр|подскаж)');

// Маркеры близости / времени / наличия — характерны для поиска по каталогу.
final RegExp _catalogMarker = RegExp(
    r'(побли|поблизост|рядом|недалеко|в радиусе|\sкм|на недел|на завтра|на сегодня|'
    r'на эт[оиа]|на следующ|ближайш|на дн[ия]|свобод|есть ли|есть что|'
    r'есть\s+(заказ|работ|смен|подработк|исполнит|свобод)|какие есть|что есть|что по|'
    r'сколько (есть|заказ|вариант)|'
    r'январ|феврал|март|апрел|\sма[йя]|июн|июл|август|сентябр|октябр|ноябр|декабр|'
    r'\d{1,2}[.\-/ ]\d{1,2})');

// Упоминание конкретной техники — сильный сигнал поиска в обоих приложениях
// (исполнитель: «заказы на минитрактор»; заказчик: «нужен экскаватор»).
final RegExp _machineryMention = RegExp(
    r'(экскаватор|экскаватр|миниэкскаватор|самосвал|(^|\s)кран|автокран|бульдозер|'
    r'погрузчик|минипогрузчик|фронтальн|манипулятор|кму|самогруз|автовышк|вышк|'
    r'(^|\s)трактор|минитрактор|грейдер|каток|бетононасос|миксер|ассенизатор|'
    r'эвакуатор|бурени|буроям|ямобур|гидромолот|(^|\s)трал|'
    r'бобкат|bobcat|jcb|джисиби|джейсиби|котлован|копа[тею])');

// Предмет поиска у исполнителя — заказы/работа/смены.
final RegExp _orderSubject =
    RegExp(r'(заказ|подработк|смен[ауыой]|объект|халтур|рейс|\sработ[ауыой])');

// Предмет поиска у заказчика — исполнитель/техника. Список техники компактный,
// покрывает массовые типы; остальное всё равно ловится по слову «исполнитель»
// или маркерам.
final RegExp _executorSubject = RegExp(
    r'(исполнител|подрядчик|мастер|специалист|бригад|спецтехник|\sтехник|'
    r'экскаватор|самосвал|(^|\s)кран|бульдозер|погрузчик|манипулятор|автовышк|'
    r'трактор|грейдер|каток|бетононасос|миксер|ассенизатор|эвакуатор|бурени|'
    r'ямобур|гидромолот|(^|\s)трал|автокран|фронтальн|кму|вышк)');

// Уточнения поиска — они должны ОСТАВАТЬСЯ в поиске, а не уводить назад в FAQ.
final RegExp _refine = RegExp(
    r'(дешевл|дорож|подешевл|подорож|ближ|дальш|подальш|поближ|ещё|еще|'
    r'други|только|радиус|за город|подробн)');

/// Похоже ли сообщение на запрос поиска по каталогу.
/// [isCustomer] — приложение заказчика (ищет исполнителей/технику), иначе
/// приложение исполнителя (ищет заказы).
bool looksLikeCatalogSearch(String raw, {required bool isCustomer}) {
  final String t = ' ${raw.toLowerCase().trim()} ';
  if (t.trim().length < 4) return false;

  final RegExp subject = isCustomer ? _executorSubject : _orderSubject;
  final bool hasSubject = subject.hasMatch(t);
  final bool hasVerb = _searchVerb.hasMatch(t);
  final bool hasMarker = _catalogMarker.hasMatch(t);
  final bool hasMachinery = _machineryMention.hasMatch(t);

  // Определительные / how-to вопросы — это FAQ ВСЕГДА, даже если рядом мелькнула
  // техника («что такое jcb», «как создать заказ»). Их не уводим в поиск.
  if (_faqHowto.hasMatch(t)) return false;
  // «Сколько стоит подписка» (без техники) — FAQ; «сколько стоит экскаватор»
  // (техника есть) — поиск с фильтром по цене, поэтому при технике НЕ режем.
  if (_priceQuery.hasMatch(t) && !hasMachinery) return false;

  // ВАЖНО: служебные слова (мои/у меня, документы/телефон/карточка/профиль,
  // рейтинг/оплата/тариф) уводят в FAQ ТОЛЬКО когда техника НЕ названа. Иначе
  // «нужен экскаватор, есть документы», «кран с оплатой по телефону»,
  // «экскаватор для моего объекта» ошибочно глохли как «вопрос про аккаунт».
  if (!hasMachinery) {
    if (_possessive.hasMatch(t)) return false;
    if (_appAccount.hasMatch(t)) return false;
    // Рейтинг/оплата/тариф без техники и без «найди + предмет» — про аккаунт
    // («как поднять рейтинг»), а не фильтр.
    if (_appFilter.hasMatch(t) && !(hasVerb && hasSubject)) return false;
  }

  // Явно названная техника — почти всегда поиск. Покрывает «нужен экскаватор»,
  // «кран на завтра в москве», «джисиби», «бобкат рядом».
  if (hasMachinery) return true;
  // Сильные сигналы поиска без явной техники.
  if (hasVerb && hasSubject) return true;
  if (hasSubject && hasMarker) return true;
  return false;
}

/// Явное намерение СОЗДАТЬ заказ текстом («создай заказ на экскаватор», «оформи
/// заявку», «хочу разместить заказ») — чтобы из обычного чата увести в пошаговый
/// сбор (slot-fill), а не в FAQ-инструкцию или поиск. Проверяется ДО поиска.
/// «Как создать заказ?» — это FAQ (вопрос-инструкция), его НЕ перехватываем.
bool looksLikeCreateOrder(String raw) {
  final String t = ' ${raw.toLowerCase().trim()} ';
  if (_faqHowto.hasMatch(t)) return false;
  final bool createVerb = RegExp(
    r'(созда[й]|создат|оформ|размест|опубликов|выложи|выклад|заведи|завест|сформиру)')
      .hasMatch(t);
  final bool orderObject = RegExp(r'(заказ|заявк)').hasMatch(t);
  if (createVerb && orderObject) return true;
  // «хочу/нужно создать/оформить заказ» — тот же интент без названной техники.
  if (RegExp(r'(хочу|хочется|нужно|надо|хотел[аи]?\s+бы)\s+\S*\s*'
      r'(созда|оформ|размест|опубликов)').hasMatch(t)) {
    return true;
  }
  return false;
}

/// Похоже ли сообщение на FAQ/вопрос про аккаунт — чтобы в режиме поиска
/// вернуться в обычный чат. Уточнения поиска (подешевле/поближе/…) сюда не
/// попадают и остаются в поиске.
bool looksLikeFaqQuestion(String raw) {
  final String t = ' ${raw.toLowerCase().trim()} ';
  // Сильный FAQ-сигнал (вопрос-инструкция / тема аккаунта) выводит из поиска
  // ДАЖЕ если в фразе мелькнуло уточняющее слово («подробнее, как пройти
  // верификацию»). Поэтому проверяем его ДО _refine.
  if (_faqHowto.hasMatch(t) || _appAccount.hasMatch(t)) return true;
  // Уточнения (подешевле/поближе/радиус/«мой бюджет 5000»…) ОСТАЮТСЯ в поиске.
  if (_refine.hasMatch(t)) return false;
  // «Мои заказы», «у меня рейтинг» — вопрос про свои данные → в чат. А «мой
  // бюджет 5000» (без личного существительного) остаётся уточнением поиска.
  if (_possessive.hasMatch(t) && _personalData.hasMatch(t)) return true;
  // «Сколько стоит подписка» (ценовой FAQ без техники) — выводим в FAQ.
  if (_priceQuery.hasMatch(t) && !_machineryMention.hasMatch(t)) return true;
  return false;
}

/// Жёсткий FAQ-сигнал именно для ВЫХОДА из пошагового создания заказа/услуги
/// (slot-fill). Тут НЕ учитываем притяжательные слова («моё/мой/у меня»): в
/// слот-филле это нормальные ОТВЕТЫ («у меня в Москве», «моё местоположение»,
/// «мой адрес — …»), а не вопрос. Раньше из-за этого «моё местоположение»
/// выкидывало из создания заказа в обычный чат, ассистент отвечал «не могу
/// определить местоположение», а собранный черновик и геопозиция терялись.
/// Выходим из сбора только на настоящий вопрос-инструкцию («как отменить?»,
/// «сколько стоит подписка») или тему про аккаунт (подписка/верификация/…).
bool looksLikeFaqInterruption(String raw) {
  final String t = ' ${raw.toLowerCase().trim()} ';
  // Явная отмена/выход из сбора («стоп», «отмена», «выйти», «передумал»).
  if (_cancelCmd.hasMatch(t)) return true;
  // «Как обычно / как можно скорее / как лучше» — это ОТВЕТ про сроки или
  // «на твоё усмотрение», а не FAQ-вопрос. Сбор НЕ прерываем.
  if (_howAdverbAnswer.hasMatch(t)) return false;
  // Настоящий вопрос-инструкция («как отменить?», «почему…», «что такое…»)
  // выводит из сбора даже при уточняющем слове.
  if (_faqHowto.hasMatch(t)) return true;
  // Тема аккаунта (подписка/верификация/карточка/телефон…) выводит из сбора
  // ТОЛЬКО если это похоже на ВОПРОС. Простое упоминание этих слов в ответе —
  // не повод рвать сбор (иначе при заполнении карточки слово «карточка»
  // выкидывало в обычный чат с потерей черновика).
  if (_appAccount.hasMatch(t) && _questionMarker.hasMatch(t)) return true;
  if (_refine.hasMatch(t)) return false;
  // Ценовой вопрос («сколько стоит подписка») — это вопрос, а не ответ-слот.
  if (_priceQuery.hasMatch(t)) return true;
  return false;
}

/// Явная просьба о поиске посреди пошагового сбора — чтобы выйти из создания
/// и увести в поиск, а не трактовать «лучше найди готовых исполнителей» как
/// очередной ответ-слот.
bool looksLikeSearchInterruption(String raw, {required bool isCustomer}) =>
    looksLikeCatalogSearch(raw, isCustomer: isCustomer);
